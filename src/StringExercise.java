public class StringExercise {
    public static void main(String[] args) {
        String pfloyd1 = "We don't need no education\nWe don't need no thought control.";
        System.out.println(pfloyd1);
        // "\n" is new line
        System.out.println("//////////");
        String inNout = "Check \"this\" out!, \"s inside of \"s";
        System.out.println(inNout);
        System.out.println("//////////");
        String windows = "In windows, the main drive is usually C:\\";
        System.out.println(windows);
        System.out.println("//////////");
        String backSlash = "I can do backslashes \\, double backslashes \\\\,\n" +
                "and the amazing triple backslash \\\\\\!";
        System.out.println(backSlash);






    }
}
